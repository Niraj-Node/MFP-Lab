DATA SEGMENT
  NUM DW 0005,0007
  LCM DW 2 DUP(0)
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	MOV AX,DATA
	MOV DS,AX

	MOV SI, OFFSET NUM
	MOV DI, OFFSET LCM
	MOV AX, WORD PTR [SI]
	MOV BX, WORD PTR [SI+2]
	MOV CX, AX
	MOV DX, BX

	repeat: NOP
	CMP AX, BX
	JE STORE
	JC ASMALLER
	ADD BX, DX
	JMP repeat
	ASMALLER: NOP
	ADD AX, CX
	JMP repeat

	STORE: NOP
	MOV WORD PTR[DI], AX
	MOV AX, 4C00H
	INT 21H
CODE ENDS
END